#!/usr/bin/env python3
"""
Build and bundle ArduPlane firmware and associated configuration for AFQT.

This script builds the flight controller firmware for a specific aircraft
configuration, prepares the necessary files, and organizes them into a
directory structure suitable for firmware upload through AFQT (Aircraft Final
Qualification Tool).

This script does not build AP_Periph, which is handled by build_cpns.py.
"""
import shutil
import argparse
from pathlib import Path

import common
from common import CXPILOT_ROOT, CXPILOT_CORE_ROOT

ROMFS_CUSTOM_DIR = CXPILOT_CORE_ROOT / "ROMFS_custom"

BUILD_TMP_DIR = CXPILOT_ROOT / "build"
EXTRA_HWDEF = BUILD_TMP_DIR / "extra_hwdef.dat"
DEFAULTS_FILE = BUILD_TMP_DIR / "defaults.parm"

# Import the configuration abstraction tools
common.add_config_tools_to_path()
import ac_config_tools  # noqa: E402


# Organize the build output files
def organize_output(config_name: str, xml_file: Path, fc_firmware_name: str) -> None:
    """Organize build output files into a directory structure for AFQT.

    The output directory structure is as follows:
    output/
    ├── <config_name>/
    │   ├── <fc_firmware_name>/
    │   │   ├── <firmware_binaries>
    │   │   ├── ...
    │   │   ├── defaults.parm
    │   │   ├── extra_hwdef.dat
    │   │   └── ROMFS_custom/
    │   │       └── ...
    │   ├── <peripheral_1>/ (not generated by this script)
    │   ├── <peripheral_2>/ (not generated by this script)
    │   ├── ...
    │   ├── ReleaseNotes.txt
    │   └── AFQT/
    │       └── target.xml

    Args:
        xml_file (str): Path to the XML file.
        fc_firmware_name (str): Name of the flight controller firmware binary.
    """
    output_dir = CXPILOT_ROOT / "output"

    final_output_dir = output_dir / config_name
    final_output_dir.mkdir(parents=True, exist_ok=True)
    print(f"Output directory created at {final_output_dir}")

    fc_output_dir = final_output_dir / fc_firmware_name
    shutil.rmtree(fc_output_dir, ignore_errors=True)

    # Move the firmware binary to the output directory
    build_dir = CXPILOT_CORE_ROOT / "build" / fc_firmware_name
    firmware_bin = build_dir / "bin"
    if not firmware_bin.exists():
        raise FileNotFoundError(
            f"Flight controller firmware binary not found for {fc_firmware_name} in {build_dir.name}"
        )
    shutil.copytree(firmware_bin, fc_output_dir)
    print(f"Moved {firmware_bin} binaries to {fc_output_dir}")

    # Move the processed defaults file to the output directory
    defaults_file = fc_output_dir / "defaults.parm"
    shutil.copy(DEFAULTS_FILE, defaults_file)

    # Copy the extra_hwdef.dat file to the output directory
    shutil.copy(EXTRA_HWDEF, fc_output_dir)

    # Copy the ROMFS_custom directory to the output directory
    if not ROMFS_CUSTOM_DIR.exists():
        raise FileNotFoundError("ROMFS_custom directory not found.")
    romfs_custom_dest = fc_output_dir / "ROMFS_custom"
    shutil.copytree(ROMFS_CUSTOM_DIR, romfs_custom_dest)

    # move Release Notes to the output directory
    release_notes = CXPILOT_ROOT / "ReleaseNotes.txt"
    if not release_notes.exists():
        raise FileNotFoundError("ReleaseNotes.txt not found!")
    shutil.copy(release_notes, final_output_dir)
    print(f"Moved ReleaseNotes.txt to {final_output_dir}")

    # create a directory AFQT and Rename the xml file to target.xml and move it to the output directory
    target_xml = final_output_dir / "AFQT" / "target.xml"
    target_xml.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy(xml_file, target_xml)
    print(f"Moved {xml_file} to {target_xml}")


def build_flight_controller_firmware(
        board_name: str,
        env: dict | None = None,
        debug: bool = False,
        upload: bool = False) -> None:
    """Build ArduPlane firmware for the flight controller.

    Args:
        board_name (str): Name of the board in hwdef, e.g., CubeOrange-CX
        env (dict, optional): Environment variables to set for the build.
        debug (bool): If True, build in debug mode.
        upload (bool): If True, upload the firmware after building.
    """
    waf_args = [
        "./waf", "configure",
        f"--board={board_name}",
        f"--default-parameters={DEFAULTS_FILE}",
        f"--extra-hwdef={EXTRA_HWDEF}",
        "--debug-symbols"
    ]
    if debug:
        waf_args.append("--debug")
    common.run_cmd(waf_args, cwd=CXPILOT_CORE_ROOT, env=env)

    waf_args = ["./waf", "plane"]
    if upload:
        waf_args.append("--upload")
    common.run_cmd(waf_args, cwd=CXPILOT_CORE_ROOT, env=env)


def build_config(config_name: str, debug: bool = False, upload: bool = False) -> None:
    """Build and organize output for a single configuration."""
    print(f"\n=== Building {config_name} ===")

    # Get environment variables to embed the correct git hash in the firmware
    env = ac_config_tools.get_flight_controller_env()

    # Clean up any previous ROMFS_custom directory
    shutil.rmtree(ROMFS_CUSTOM_DIR, ignore_errors=True)

    # Prep the extra files and directories needed for the build
    EXTRA_HWDEF.unlink(missing_ok=True)
    DEFAULTS_FILE.unlink(missing_ok=True)
    lua_scripts_dir = ROMFS_CUSTOM_DIR / "scripts"
    lua_scripts_dir.mkdir(parents=True, exist_ok=True)
    xml_out = ROMFS_CUSTOM_DIR / "AircraftConfiguration.xml"

    ac_config_tools.prepare_flight_controller(
        config=config_name,
        version=common.get_cx_version(),
        xml_out=xml_out,
        scripts_out=lua_scripts_dir,
        defaults_out=DEFAULTS_FILE,
        extra_hwdef_out=EXTRA_HWDEF,
        strip_defaults=True
    )
    fc_board_name = ac_config_tools.get_fc_board_name(config_name)

    build_flight_controller_firmware(fc_board_name, env=env, debug=debug, upload=upload)
    organize_output(config_name, xml_out, fc_board_name)

    print('Done')


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--config", "-c",
        action="append",
        metavar="CONFIG",
        help="Limit to specific aircraft configuration(s) (default: all configs)."
    )
    parser.add_argument('--keep-romfs-custom', action='store_true', help='Keep the ROMFS_custom directory after building')
    parser.add_argument('--require-clean-git', action='store_true', help='Fail if any git repo is dirty')
    parser.add_argument('--debug', action='store_true', help='Enable debug build')
    parser.add_argument('--upload', action='store_true', help='Upload the firmware to the flight controller')
    parser.add_argument('--waf-clean', action='store_true', help='Run waf clean before building')
    parser.add_argument('--waf-distclean', action='store_true', help='Run waf distclean before building')
    parser.add_argument('--list', action='store_true', help='List all detected configs (inc. deprecated if allowed) and exit')
    parser.add_argument('--allow-deprecated', action='store_true', help='Allow deprecated configurations to be listed')
    parser.add_argument('--clean', action='store_true', help='clean the build/output directory before building')
    args = parser.parse_args()

    configs = ac_config_tools.get_configs(allow_deprecated=args.allow_deprecated)

    # List configs and exit
    if args.list:
        for c in configs:
            print(c)
        return

    if args.require_clean_git:
        common.assert_clean_git_all()

    # Clean the output directory if requested
    if args.clean:
        shutil.rmtree(CXPILOT_ROOT / "build", ignore_errors=True)
        shutil.rmtree(CXPILOT_ROOT / "output", ignore_errors=True)
    if args.waf_clean:
        common.run_cmd(["./waf", "clean"], cwd=CXPILOT_CORE_ROOT)
    if args.waf_distclean:
        common.run_cmd(["./waf", "distclean"], cwd=CXPILOT_CORE_ROOT)

    # If config is provided, build just that config
    if args.config:
        # Make sure the config name is valid. We fetch configs again, this time
        # forcing the allow_deprecated to True. If the user has specified a
        # config, we assume they want it regardless of its deprecation status.
        all_configs = ac_config_tools.get_configs(allow_deprecated=True)
        for config in args.config:
            if config not in all_configs:
                raise ValueError(f"Configuration '{config}' not found. Available configurations: {', '.join(all_configs)}")
        configs = args.config

    # Build all detected configs
    for xml_file in configs:
        build_config(xml_file, debug=args.debug, upload=args.upload)

    # Leave the last ROMFS_custom directory if requested
    if not args.keep_romfs_custom:
        shutil.rmtree(ROMFS_CUSTOM_DIR, ignore_errors=True)


if __name__ == '__main__':
    main()
