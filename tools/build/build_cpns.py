#!/usr/bin/env python3
"""
Build and generate AP_Periph firmware for CAN peripheral nodes defined in cxpilot aircraft configurations.
"""

import shutil
import argparse
from typing import Iterable

import common
from common import CXPILOT_ROOT, CXPILOT_CORE_ROOT

BASE_BUILD_DIR = CXPILOT_ROOT / "build" / "base_builds"
FINAL_BUILD_DIR = CXPILOT_ROOT / "build" / "final_builds"

# Import the configuration abstraction tools
common.add_config_tools_to_path()
import ac_config_tools  # noqa: E402


def get_base_builds(configs: Iterable[str]) -> list[str]:
    """
    Get a list of unique base firmware builds required for the given aircraft configurations.

    Args:
        configs (Iterable[str]): List of aircraft configuration names.

    Returns:
        list[str]: List of unique base firmware names.
    """
    return ac_config_tools.get_periph_bases(configs=configs)


def build_base_firmware(base_name: str):
    """
    Compile a specific base AP_Periph firmware with waf.

    CPN build CI would take forever if we just rebuild the dozens of unique
    final binaries for each CPN. We use this to bring the number of waf builds
    down to about three. Then we come back and modify the built firmware
    with the correct board name and default parameters.

    Args:
        base_name (str): Name of the base firmware to build (e.g., "CarbonixF405_no-crystal").
    """
    # Get environment variables for git hash to embed in the firmware
    env = ac_config_tools.get_periph_env()

    extra_hwdef = BASE_BUILD_DIR / f"{base_name}.hwdef.dat"
    extra_hwdef.unlink(missing_ok=True)  # Remove if it exists

    ac_config_tools.prepare_base_periph_firmware(
        base_name=base_name,
        version=common.get_cx_version(),
        extra_hwdef_out=extra_hwdef,
    )
    waf_board = ac_config_tools.get_waf_board(base_name)

    waf_args = [
        "./waf", "configure",
        f"--board={waf_board}",
        f"--extra-hwdef={extra_hwdef.resolve()}",
    ]
    common.run_cmd(waf_args, cwd=CXPILOT_CORE_ROOT, env=env)
    common.run_cmd(["./waf", "AP_Periph"], cwd=CXPILOT_CORE_ROOT, env=env)

    # Copy the built firmware to the base build directory
    # binary = os.path.join(CXPILOT_CORE_ROOT, 'build', waf_board, 'bin', 'AP_Periph.bin')
    binary = CXPILOT_CORE_ROOT / "build" / waf_board / "bin" / "AP_Periph.bin"
    if not binary.exists():
        raise FileNotFoundError(f"Built firmware not found: {binary}")
    dest = BASE_BUILD_DIR / f"{base_name}.bin"
    shutil.copy(binary, dest)


def generate_final_cpns(config_name: str) -> None:
    """
    Generate final binaries for each CPN in the specified aircraft config.

    This embeds the correct board name and defaults into the AP_Periph binary.
    Args:
        config (str): Aircraft configuration name
    """
    # Remove any previous build output
    shutil.rmtree(FINAL_BUILD_DIR, ignore_errors=True)

    ac_config_tools.generate_cpn_firmware(
        config=config_name,
        base_dir=BASE_BUILD_DIR,
        out_dir=FINAL_BUILD_DIR,
    )


def organize_output(config_name: str) -> None:
    """
    Organize build output files into a directory structure for AFQT.

    The output directory structure is as follows:
    output/
    ├── <config_name>/
    │   ├── <peripheral_1>/
    │   │   ├── AP_Periph.bin
    │   │   ├── defaults.parm
    │   ├── <peripheral_2>/
    │   │   ├── AP_Periph.bin
    │   │   ├── defaults.parm
    │   ├── ...
    |   ├── (other stuff generated by build_aircraft.py)
    │   ├── ...
    """
    if not FINAL_BUILD_DIR.exists():
        raise FileNotFoundError(
            f"Final build directory '{FINAL_BUILD_DIR}' does not exist. Generate CPN firmware first using --generate."
        )
    if not any(FINAL_BUILD_DIR.iterdir()):
        raise ValueError(
            f"Final build directory '{FINAL_BUILD_DIR}' is empty. No CPN firmware generated."
        )

    # for filename in os.listdir(FINAL_BUILD_DIR):
    for filepath in FINAL_BUILD_DIR.iterdir():
        output_dir = CXPILOT_ROOT / "output" / config_name
        filename = filepath.name
        dest = None
        if filename.endswith('.bin'):
            cpn_name = filename.replace('.bin', '')
            dest = output_dir / cpn_name / 'AP_Periph.bin'
        if filename.endswith('.defaults.parm'):
            cpn_name = filename.replace('.defaults.parm', '')
            dest = output_dir / cpn_name / 'defaults.parm'
        if dest is not None:
            dest.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy(filepath, dest)


def main():
    parser = argparse.ArgumentParser(
        description="Build and generate AP_Periph firmware for CAN peripheral nodes."
    )
    parser.add_argument(
        "--config", "-c",
        action="append",
        metavar="CONFIG",
        help="Limit to specific aircraft configuration(s) (default: all configs)."
    )
    parser.add_argument(
        "--allow-deprecated",
        action="store_true",
        help="When using all configs, include deprecated configurations."
    )
    parser.add_argument(
        "--clean",
        action="store_true",
        help="Clean output/build directories before building."
    )
    parser.add_argument("--waf-clean", action='store_true', help='Run waf clean before building')
    parser.add_argument("--waf-distclean", action='store_true', help='Run waf distclean before building')
    parser.add_argument(
        "--require-clean-git",
        action="store_true",
        help="Fail if any git repository is dirty."
    )
    parser.add_argument(
        "--list-bases",
        action="store_true",
        help="List all unique base firmware builds (before modifying with board name and defaults)."
    )
    parser.add_argument("--json", action="store_true", help="Use with --list-bases to output the list in JSON format.")
    parser.add_argument(
        "--build-base",
        action="append",
        nargs="?",
        default=None,
        metavar="TARGET",
        help="Build a specific base firmware by name (get from --list output). If none, builds all for the specified configs.",
    )
    parser.add_argument(
        "--generate",
        action="store_true",
        help="Generate final CPN binaries for the specified configs."
    )
    args = parser.parse_args()

    if args.config:
        configs = ac_config_tools.get_configs(allow_deprecated=True)
        for config in args.config:
            if config not in configs:
                raise ValueError(f"Configuration '{config}' not found.")
        configs = args.config
    else:
        configs = ac_config_tools.get_configs(allow_deprecated=args.allow_deprecated)

    if args.list_bases:
        if args.json:
            import json
            print(json.dumps(get_base_builds(configs)))
            return
        for base_build in get_base_builds(configs):
            print(base_build)
        return

    if args.require_clean_git:
        common.assert_clean_git_all()

    # Clean the output directory if requested
    if args.clean:
        shutil.rmtree(CXPILOT_ROOT / "build", ignore_errors=True)
        shutil.rmtree(CXPILOT_ROOT / "output", ignore_errors=True)
    if args.waf_clean:
        common.run_cmd(["./waf", "clean"], cwd=CXPILOT_CORE_ROOT)
    if args.waf_distclean:
        common.run_cmd(["./waf", "distclean"], cwd=CXPILOT_CORE_ROOT)

    if args.build_base is None:
        # Don't build anything
        build_base = []
    elif None in args.build_base:
        # Build all for the specified configs
        build_base = get_base_builds(configs)
    else:
        # Build only the specified base firmware builds
        build_base = args.build_base

    for base_name in build_base:
        print(f"Building base firmware: {base_name}")
        build_base_firmware(base_name)

    if args.generate:
        for config_name in configs:
            print(f"Generating final CPN firmware for config: {config_name}")
            generate_final_cpns(config_name)
            organize_output(config_name)


if __name__ == "__main__":
    main()
